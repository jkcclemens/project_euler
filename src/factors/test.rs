extern crate test;

use factors::{factors, PrimeFactors};

#[test]
fn factors_128() {
  assert_eq!(vec![1, 2, 4, 8, 16, 32, 64, 128], factors(128));
}

#[test]
fn prime_factors_128() {
  assert_eq!(vec![2; 7], PrimeFactors::new(128).collect::<Vec<_>>());
}

